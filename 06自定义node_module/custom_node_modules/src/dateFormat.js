
function dateFormat(dateStr) {
    const Y = dateStr.getFullYear()
    const M = dateStr.getMonth() + 1
    const D = dateStr.getDate()
    const h = repair(dateStr.getHours())
    const m = repair(dateStr.getMinutes())
    const s = repair(dateStr.getSeconds())
    return `${Y}-${M}-${D} ${h}:${m}:${s}`
}

function repair(str) {
    return str <= '9' ? '0' + str : str
}
module.exports = {
    dateFormat
}